'''
Escreva um programa para calcular a área de um triângulo, dados os valores de base e altura.
'''
b = float(input('informe o valor da base do triangulo: '))
h = float(input('informe o valor da altura do triângulo: '))
a = (b * h)/2
print('A área do triângulo de base',b,'e altura',h,'é',a)
